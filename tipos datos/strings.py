cadena1 = "Hola soy Benja"
cadena2 = " Bienvenido gigachad"

# concateno las strings y convierto a mayuscula
resultado = cadena1.upper() + cadena2.upper()

print(resultado)

# concateno las strings y convierto a minuscula
resultado2 = cadena1.lower() + cadena2.lower()

print(resultado2)

# buscar una cadena dentro de otra

busqueda = resultado.find("D")

print(busqueda)

# buscar una cadena dentro de otra por index

busqueda2 = resultado.index("D")

print(busqueda2)


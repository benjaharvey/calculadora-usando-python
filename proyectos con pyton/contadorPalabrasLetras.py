# Escribe un programa que reciba un texto como entrada y cuente la cantidad de palabras o letras.

palabra = input("Ingrese una palabra: ")

print("La cantidad de letras de la palabra ingresada es: " + str(len(palabra)))


palabras = input("Ingrese una frase: ")

print("La cantidad de palabras de la frase ingresada es: " + str(len(palabras.split())))

